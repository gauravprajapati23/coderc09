function cc1() {
      var contentToCopy = document.getElementById('id1').innerText;
      navigator.clipboard.writeText(contentToCopy).then(function() {
        alert('Content copied to clipboard!');
      }, function(err) {
        console.error('Failed to copy: ', err);
      });
    }
    
    
